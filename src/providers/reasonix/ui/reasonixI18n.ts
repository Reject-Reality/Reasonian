export function trReasonix(
  key: ReasonixTranslationKey,
  params?: Record<string, string | number>,
): string {
  const locale = getLocale();
  const template = REASONIX_TRANSLATIONS[locale]?.[key] ?? EN_US[key] ?? key;

  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, param) => {
    return params[param]?.toString() ?? `{${param}}`;
  });
}
